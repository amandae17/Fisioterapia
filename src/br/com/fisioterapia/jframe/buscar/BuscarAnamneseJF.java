    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String cpfB1[] = txtCpfBusca.getText().split("-");
        String cpfB2[] = cpfB1[0].split("\\.");
        String testeCpfB = cpfB2[0] + cpfB2[1] + cpfB2[2] + cpfB1[1];
        long cpfB;

        if (!testeCpfB.isBlank()) {
            cpfB = Long.parseLong(testeCpfB);
            Paciente p1 = new Paciente(cpfB);
            if (Anamnese.verificarAnamnese(cpfB)) {//Verificar se o paciente possui alguma anamnese
                p1 = Paciente.BuscarPaciente(cpfB);
                txtPacienteNome.setText(p1.getNome());
                Anamnese a1 = Anamnese.buscarAnamneseIdPessoa(p1.getCodPessoa());
                spnPeso.setValue(a1.getPeso());
                spnAltura.setValue(a1.getAltura());
                txtDoencasInfancia.setText(a1.getDoencaInfancia());
                txtDoencasPrevias.setText(a1.getDoencaPrevia());
                ///Tabagismo
                if (a1.getTabagista().equals("Nunca")) {
                    lblDataTabajista.setVisible(false);
                    txtDataTabagista.setVisible(false);
                    lblTipoTabagismo.setVisible(false);
                    txtTipoTabagismo.setVisible(false);
                    txtTabagismoStatus.setText("Nunca fumou");
                } else if (a1.getTabagista().equals("Sim")) {
                    Tabagismo t1 = Tabagismo.buscarTabagismoIdAnamnese(a1.getIdAnamnese());
                    lblDataTabajista.setVisible(true);
                    txtDataTabagista.setVisible(true);
                    lblTipoTabagismo.setVisible(true);
                    txtTipoTabagismo.setVisible(true);
                    txtDataTabagista.setText(t1.getDataTabagismo().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                    txtTipoTabagismo.setText(t1.getTipo());
                    txtTabagismoStatus.setText("Fumante");
                } else if (a1.getTabagista().equals("Não")) {
                    Tabagismo t1 = Tabagismo.buscarTabagismoIdAnamnese(a1.getIdAnamnese());
                    lblDataTabajista.setVisible(true);
                    txtDataTabagista.setVisible(true);
                    lblTipoTabagismo.setVisible(true);
                    txtTipoTabagismo.setVisible(true);
                    txtDataTabagista.setText(t1.getDataTabagismo().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                    txtTipoTabagismo.setText(t1.getTipo());
                    txtTabagismoStatus.setText("Ex-fumante");
                }
                ///Etilismo
                if (a1.getEtilista().equals("Nunca")) {
                    lblEtilistaData.setVisible(false);
                    txtDataEtilista.setVisible(false);
                    lblTipoEtilista.setVisible(false);
                    txtTipoEtilista.setVisible(false);
                    txtEtilistaStatus.setText("Nunca bebeu");
                } else if (a1.getEtilista().equals("Sim")) {
                    Etilista e1 = Etilista.buscarEtilistaIdAnamnese(a1.getIdAnamnese());
                    lblEtilistaData.setVisible(true);
                    txtDataEtilista.setVisible(true);
                    lblTipoEtilista.setVisible(true);
                    txtTipoEtilista.setVisible(true);
                    txtDataEtilista.setText(e1.getDataEtilista().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                    txtTipoEtilista.setText(e1.getTipo());
                    txtEtilistaStatus.setText("Consome bebidas alcoólicas");
                } else if (a1.getEtilista().equals("Não")) {
                    Etilista e1 = Etilista.buscarEtilistaIdAnamnese(a1.getIdAnamnese());
                    lblEtilistaData.setVisible(true);
                    txtDataEtilista.setVisible(true);
                    lblTipoEtilista.setVisible(true);
                    txtTipoEtilista.setVisible(true);
                    txtDataEtilista.setText(e1.getDataEtilista().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                    txtTipoEtilista.setText(e1.getTipo());
                    txtEtilistaStatus.setText("Parou de beber");
                }
                ///Cirurgia:
                if (a1.verificarCirurgia()) {
                    Cirurgia c1 = Cirurgia.buscarCirurgiaIdAnamnese(a1.getIdAnamnese());
                    lblCausaCirurgia.setVisible(true);
                    txtCausaCirurgia.setVisible(true);
                    lblDataCirurgia.setVisible(true);
                    txtDataCirurgia.setVisible(true);
                    lblLocalCirurgia.setVisible(true);
                    txtLocalCirurgia.setVisible(true);
                    txtCausaCirurgia.setText(c1.getCausa());
                    txtDataCirurgia.setText(c1.getData().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                    txtLocalCirurgia.setText(c1.getLocal());
                    txtCirurgiaStatus.setText("Dados da cirurgia:");
                } else {
                    lblCausaCirurgia.setVisible(false);
                    txtCausaCirurgia.setVisible(false);
                    lblDataCirurgia.setVisible(false);
                    txtDataCirurgia.setVisible(false);
                    lblLocalCirurgia.setVisible(false);
                    txtLocalCirurgia.setVisible(false);
                    txtCirurgiaStatus.setText("Não possui cirurgia prévia");
                }
                lblSaida.setText("Anamnese encontrada");
            } else {
                lblSaida.setText("Não foi encontrada uma anamnese para o paciente com esse cpf");
            }
        } else {
            lblSaida.setText("Campo em branco");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

