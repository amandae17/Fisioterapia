        float altura = (float) spnAltura.getValue();
        float imc1;
        if (altura > 0) {
            imc1 = peso / (altura * altura);
            String imc = String.format("%.1f", imc1);
            txtImc.setText(imc);
        } else {
            txtImc.setText(Float.toString(0));
        }
