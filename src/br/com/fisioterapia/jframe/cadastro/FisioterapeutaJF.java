    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String endereco = txtEndereco.getText();
        String usuario = txtUsuario.getText();
        String senha = new String(txtSenha.getPassword());

        String rg1[] = txtRg.getText().split("-");
        String rg2[] = rg1[0].split("\\.");
        String rg = rg2[0] + rg2[1] + rg2[2] + rg1[1];

        String cpf1[] = txtCpf.getText().split("-");
        String cpf2[] = cpf1[0].split("\\.");
        String testeCpf = cpf2[0] + cpf2[1] + cpf2[2] + cpf1[1];
        long cpf;

        String telefone1[] = txtTelefone.getText().split("-");
        long telefone;

        String crefito1[] = txtCrefito.getText().split("-");
        String crefito = crefito1[0] + crefito1[1];

        if (!nome.isBlank() && !email.isBlank() && !endereco.isBlank() && !rg.isBlank() && !testeCpf.isBlank() && !telefone1[0].isBlank() && !telefone1[1].isBlank() && !crefito.isBlank() && !usuario.isBlank() && !senha.isBlank()) {
            cpf = Long.parseLong(testeCpf);

            String telefone2[] = telefone1[0].split(" ");
            String testeTelefone = telefone2[0] + telefone2[1] + telefone1[1];

            telefone = Long.parseLong(testeTelefone);

            Fisioterapeuta f1 = new Fisioterapeuta(crefito, usuario, senha, nome, cpf, rg, telefone, email, endereco);

            if (!f1.verificarCpf()) {
                if (!f1.verificarUsuario()) {
                    if (!f1.verificarCrefito()) {
                        f1.cadastrarPessoaFisioterapeuta();
                        f1.setCodPessoa(f1.buscarCodPessoa(cpf));
                        f1.cadastrarFisioterapeuta();
                        lblSaida.setText("Fisioterapeuta cadastrado com sucesso!!!");
                    } else {
                        lblSaida.setText("Crefito j√° cadastrado!!!");
                    }
                } else {
                    lblSaida.setText("Usu√°rio j√° cadastrado!!!");
                }

            } else {
                lblSaida.setText("CPF j√° cadastrado!!!");
            }

        } else {
            lblSaida.setText("Campos em branco!!!");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed


