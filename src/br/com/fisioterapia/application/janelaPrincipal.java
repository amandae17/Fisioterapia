    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String senha = String.valueOf(txtSenha.getPassword());
        if (!txtLogin.getText().isBlank() && !senha.isBlank()) {

            if (Proprietario.verfificarLogin(txtLogin.getText())) {
                if (Proprietario.verfificarSenha(txtLogin.getText(), senha)) {
                    menuBar.setEnabled(true);
                    menuBar.setVisible(true);
                    lblLogin.setVisible(false);
                    lblSenha.setVisible(false);
                    txtLogin.setVisible(false);
                    txtSenha.setVisible(false);
                    btnEntrar.setVisible(false);
                    lblSaida.setText("Login efetuado com sucesso");
                } else {
                    lblSaida.setText("Senha inválida");
                }
            } else if (Fisioterapeuta.verfificarLogin(txtLogin.getText())) {
                if (Fisioterapeuta.verfificarSenha(txtLogin.getText(), senha)) {
                    menuBar.setEnabled(true);
                    menuBar.setVisible(true);
                    lblLogin.setVisible(false);
                    lblSenha.setVisible(false);
                    txtLogin.setVisible(false);
                    txtSenha.setVisible(false);
                    btnEntrar.setVisible(false);

                    mnuCadastrarFisioterapeuta.setVisible(false);
                    mnuCadastrarProprietario.setVisible(false);
                    mnuCadastrarEquipamentos.setVisible(false);
                    mnuAtualizarFisioterapeuta.setVisible(false);
                    mnuAtualizarProprietario.setVisible(false);
                    mnuAtualizarEquipamentos.setVisible(false);
                    mnuBuscarFisioterapeuta.setVisible(false);
                    mnuBuscarProprietario.setVisible(false);
                    mnuBuscarEquipamentos.setVisible(false);

                    lblSaida.setText("Login efetuado com sucesso");
                } else {
                    lblSaida.setText("Senha inválida");
                }

            } else {
                lblSaida.setText("Login não encontrado");
            }
        } else {
            lblSaida.setText("Campos em branco");
        }


    }//GEN-LAST:event_btnEntrarActionPerformed

