    private void mnuLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuLogoutActionPerformed
        menuBar.setEnabled(false);
        menuBar.setVisible(false);
        lblLogin.setVisible(true);
        lblSenha.setVisible(true);
        txtLogin.setVisible(true);
        txtSenha.setVisible(true);
        btnEntrar.setVisible(true);

        mnuCadastrarFisioterapeuta.setVisible(true);
        mnuCadastrarProprietario.setVisible(true);
        mnuCadastrarEquipamentos.setVisible(true);
        mnuAtualizarFisioterapeuta.setVisible(true);
        mnuAtualizarProprietario.setVisible(true);
        mnuAtualizarEquipamentos.setVisible(true);
        mnuBuscarFisioterapeuta.setVisible(true);
        mnuBuscarProprietario.setVisible(true);
        mnuBuscarEquipamentos.setVisible(true);

        if (buscarConsultaJF.isVisible()) {
            buscarConsultaJF.setVisible(false);
        }
        if (buscarConsultasDatasJF.isVisible()) {
            buscarConsultasDatasJF.setVisible(false);
        }
        if (atualizarAnamneseJF.isVisible()) {
            atualizarAnamneseJF.setVisible(false);
        }
        if (buscarFichaJF.isVisible()) {
            buscarFichaJF.setVisible(false);
        }
        if (buscarProprietarioJF.isVisible()) {
            buscarProprietarioJF.setVisible(false);
        }
        if (buscarEquipamentoJF.isVisible()) {
            buscarEquipamentoJF.setVisible(false);
        }
        if (buscarPacienteJF.isVisible()) {
            buscarPacienteJF.setVisible(false);
        }
        if (buscarAnamneseJF.isVisible()) {
            buscarAnamneseJF.setVisible(false);
        }
        if (atualizarExercicioJF.isVisible()) {
            atualizarExercicioJF.setVisible(false);
        }
        if (atualizarProprietarioJF.isVisible()) {
            atualizarProprietarioJF.setVisible(false);
        }
        if (equipamentosJF.isVisible()) {
            equipamentosJF.setVisible(false);
        }
        if (proprietarioJF.isVisible()) {
            proprietarioJF.setVisible(false);
        }
        if (exercicioJF.isVisible()) {
            exercicioJF.setVisible(false);
        }
        if (consultaJF.isVisible()) {
            consultaJF.setVisible(false);
        }
        if (buscarFisioterapeutaJF.isVisible()) {
            buscarFisioterapeutaJF.setVisible(false);
        }

        txtLogin.setText("");
        txtSenha.setText("");
        lblSaida.setText("Logout efetuado com sucesso");
        // TODO add your handling code here:
    }//GEN-LAST:event_mnuLogoutActionPerformed

